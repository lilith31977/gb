// Задайте двумерный массив размером m×n, заполненный случайными вещественными числами.
// m = 3, n = 4.
// 0,5 7 -2 -0,2
// 1 -3,3 8 -9,9
// 8 7,8 -7,1 9

double[,] NewArray(int rowLength, int colLength, int minRange, int maxRange)
{
    var array = new double[rowLength, colLength];
    var rnd = new Random();
    for (int i = 0; i < array.GetLength(0); i++)
    {
        for (int j = 0; j < array.GetLength(1); j++)
        {
            array[i, j] = rnd.Next(minRange, maxRange + 1) + rnd.NextDouble();

        }
    }
    return array;
}

void PrintArray(double[,] array)
{
    for (int i = 0; i < array.GetLength(0); i++)
    {
        for (int j = 0; j < array.GetLength(1); j++)
        {
            System.Console.Write("{0,6:F1}", array[i, j]);
        }
        System.Console.WriteLine();
    }
    System.Console.WriteLine();
}

double[,] array = NewArray(3, 4, -10, 10);
PrintArray(array);